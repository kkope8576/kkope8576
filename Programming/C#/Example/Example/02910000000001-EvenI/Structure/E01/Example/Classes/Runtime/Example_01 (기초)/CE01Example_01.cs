/*
 * 자료구조란?
 * - 데이터를 저장하고 표현하는 방법을 의미한다. (즉, int 와 같은 기본 자료형도 넓은 의미로 보면
 * 자료구조에 포함된다는 것을 알 수 있다.)
 * 
 * 프로그래밍에서 자료구조는 일반적으로 여러 데이터를 다루는 방법을 의미한다. (즉, 자료구조를
 * 활용하면 여러 데이터를 좀 더 효율적으로 관리 및 제어하는 것이 가능하다.)
 * 
 * 자료구조는 데이터를 관리하는 방법에 따라 여러 종류가 존재하며 크게 선형 자료구조와 비선형
 * 자료구조로 구분된다.
 * 
 * 선형 자료구조 종류
 * - 리스트 (배열, 연결)
 * - 스택 / 큐
 * - 덱
 * 
 * 비선형 자료구조 종류
 * - 트리
 * - 해시
 * - 그래프
 * 
 * 위와 같이 자료구조에는 여러 종류가 존재하며 자료구조마다 데이터를 관리하는 방식이 다르기 때문에
 * 적절한 자료구조를 선택 및 활용하는 선구안이 필요하다. (즉, 선택한 자료구조에 따라 작성하는 
 * 알고리즘이 달라진다는 것을 알 수 있다.)
 */
